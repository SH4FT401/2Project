	def OnScroll(self):
		self.ScollPos = int(self.ScrollBar.GetPos() * self.BasePos + 1)
		if self.ScollPos > self.BasePos:
			self.ScollPos = self.BasePos

		self.RefreshList()

	def RefreshList(self):
		for btn in self.btnListEmoticons:
			btn.Hide()

		pos = int((self.ScollPos - 1) * self.EmoticonsCountMax)
		x = 16
		y = 38
		count = 0
		for i in xrange(len(self.btnListEmoticons)):
			if i >= pos and count < self.EmoticonsCountMax:
				if count >= 0 and count <= 10:
					self.btnListEmoticons[i].SetPosition(x + (count * self.EmoticonsStep), y)
				elif count >= 11 and count <= 21:
					self.btnListEmoticons[i].SetPosition(x + ((count - 11) * self.EmoticonsStep), y + (1 * self.EmoticonsStep))
				elif count >= 22 and count <= 32:
					self.btnListEmoticons[i].SetPosition(x + ((count - 22) * self.EmoticonsStep), y + (2 * self.EmoticonsStep))
				elif count >= 33 and count <= 43:
					self.btnListEmoticons[i].SetPosition(x + ((count - 33) * self.EmoticonsStep), y + (3 * self.EmoticonsStep))
				elif count >= 44 and count <= 54:
					self.btnListEmoticons[i].SetPosition(x + ((count - 44) * self.EmoticonsStep), y + (4 * self.EmoticonsStep))
				else:
					return
				self.btnListEmoticons[i].Show()
				count += 1
			else:
				self.btnListEmoticons[i].Hide()

	def OnPressEmoticons(self, text, newBtn = 0, category = 0, code = 0):
		for btn in self.btnNewEmoticons:
			btn.Hide()

		if newBtn > 0:
			pos = 104
			for x in xrange(6):
				main = EMOTICONS_CATEGORY_TEXT_LIST[category][code][x+1]
				main = main.replace("|T", "")
				main = main.replace("|t", "")
				tokens = main.split("/")

				btn = ui.Button()
				btn.SetParent(self)
				btn.SetUpVisual("d:/ymir work/ui/game/emoji/emoticons/%s.png" % main)
				btn.SetOverVisual("d:/ymir work/ui/game/emoji/emoticons/%s.png" % main)
				btn.SetDownVisual("d:/ymir work/ui/game/emoji/emoticons/%s.png" % main)
				btn.SetButtonScale(0.32, 0.32)
				btn.SetPosition(pos + (10 * (x + 1)), 3)
				btn.SetEvent(ui.__mem_func__(self.OnPressEmoticons), ("|c00000000|T"+str(tokens[0])+"/"+str(tokens[1])+"|t    |t|r"))
				btn.Show()
				self.btnNewEmoticons.append(btn)
				pos += 20

			return

		ime.PasteString(text)

