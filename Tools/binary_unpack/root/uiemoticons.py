					btnListEmoticons.SetEvent(ui.__mem_func__(self.OnPressEmoticons), "|c00000000|T"+tokens[1]+"|t    |t|r", 0, 0, 0)

					toolTip = ui.Ballon()
					toolTip.SetParent(btnListEmoticons)
					toolTip.SetPosition(0, -38)
					toolTip.SetWindowHorizontalAlignCenter()
					toolTip.Hide()
					if int(tokens[0]) > 1:
						toolTip.SetText(localeInfo.TOOLTIP_EMOJI_USED_MORE % str(tokens[0]))
