	def OnScroll(self):
		self.ScollPos = int(self.ScrollBar.GetPos() * self.BasePos + 1)
		if self.ScollPos > self.BasePos:
			self.ScollPos = self.BasePos

		self.RefreshList()

	def RefreshList(self):
		for btn in self.btnListEmoticons:
			btn.Hide()

		pos = int((self.ScollPos - 1) * self.EmoticonsCountMax)
		x = 16
		y = 38
		count = 0
		for i in xrange(len(self.btnListEmoticons)):
			if i >= pos and count < self.EmoticonsCountMax:
				if count >= 0 and count <= 15:
					self.btnListEmoticons[i].SetPosition(x + (count * self.EmoticonsStep), y)
				elif count >= 16 and count <= 31:
					self.btnListEmoticons[i].SetPosition(x + ((count - 16) * self.EmoticonsStep), y + (1 * self.EmoticonsStep))
				elif count >= 32 and count <= 47:
					self.btnListEmoticons[i].SetPosition(x + ((count - 32) * self.EmoticonsStep), y + (2 * self.EmoticonsStep))
				elif count >= 48 and count <= 63:
					self.btnListEmoticons[i].SetPosition(x + ((count - 48) * self.EmoticonsStep), y + (3 * self.EmoticonsStep))
				elif count >= 64 and count <= 79:
					self.btnListEmoticons[i].SetPosition(x + ((count - 64) * self.EmoticonsStep), y + (4 * self.EmoticonsStep))
				else:
					return
				self.btnListEmoticons[i].Show()
				count += 1
			else:
				self.btnListEmoticons[i].Hide()

