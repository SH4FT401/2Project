					btnListEmoticons.SetButtonScale(0.32, 0.32)
					btnListEmoticons.SetEvent(ui.__mem_func__(self.OnPressEmoticons), "|c00000000|T"+tokens[1]+"|t    |t|r", 0, 0, 0)

