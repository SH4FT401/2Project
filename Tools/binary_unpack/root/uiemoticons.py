	def SetCategory(self, category):
		for cat in self.textCategory:
			cat.Hide()
		self.textCategory[category].Show()

		self.btnListEmoticons = []
		self.btnNewEmoticons = []
		self.EmoticonsRecentList = []

		self.EmoticonsCountMax = 55
		self.EmoticonsStep = 30
		self.BasePos = 0
		self.ScollPos = 0
		self.ScrollBar.SetPos(0)

		if (category == 0):
			lines = open(EMOTICONS_FILE_NAME, "r").readlines()
