			btn.SetPosition(pos + (6 * (i + 1)), 15)
			btn.SetEvent(lambda arg = i: self.SetCategoryEmoticons(arg))
			btn.Show()

			toolTip = ui.Ballon()
			toolTip.SetParent(btn)
			toolTip.SetPosition(0, -38)
			toolTip.SetWindowHorizontalAlignCenter()
